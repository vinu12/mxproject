           <p>A liberal education has the goal of preparing students develop both practical skills such as problem-solving, ability to effectively communicate, and the ability to apply information to everyday life as well as to develop a sense of who they are as citizens of a community. This course has been designed specifically for first year students to reflect on the ethical perspectives that shape decision-making about an everyday product: apparel. The apparel industry is the context used to examine our own ethical viewpoints and the meaning of citizenship. We document how public engagement has impacted the moral issues that historically evolved within this industry and how public engagement can be used to shape and change contemporary industry practices. Specifically, students address the following: what is the concept of ethics and how does it differ from other related terms (e.g., morality, legality, self-interest, codes of conduct)? What are common ethical viewpoints and how have they developed? What ethical view(s) most closely align with their own? How did they develop their view(s)? What does it mean to behave ethically? How does a shared ethic develop within a community or organization? What is corporate culture? How does the culture of an organization promote ethical behavior? How do problems evolve within the apparel industry and what role does ethics and engagement in everyday life play in perpetuating or solving these problems?</p>
		   
		   
	   

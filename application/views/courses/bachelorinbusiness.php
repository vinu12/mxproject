		<h3>Program Outline</h3>
		
		<p>The Bachelor of Business will enable students to acquire a sound working knowledge of strategic business concepts. It will provide skills to work in a wide range of business environments or to start up a new &nbsp;business. This program integrates theory and practice, providing graduates with a choice of careers in the ever changing business world.</p>
		
		<h3>Career Opportunities</h3>
		
		<p>The Bachelor of Business provides a wide range of career options. You might expect to be employed as a business &nbsp;manager, a business consultant, business analyst, researcher or planner, &nbsp;or operate your own business</p>
		
		
		
		

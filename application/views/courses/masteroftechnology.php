		<p>Graduates of the Software Engineering stream will also have the specialist knowledge required to understand and implement advanced software engineering techniques - design, implement and maintain IT systems using a well-informed software engineering approach and create high quality software in a systematic and efficient manner. Graduates will gain employment in a wide range of industries including security and defence, education, aerospace, health, business and finance.</p>
		
		
		<h3>Career Opportunities</h3>
		
		
		<p>As a software engineer you will create, maintain and modify computer software as well as analyse current software products. You will also evaluate new programming tools and techniques. Graduates may find employment in a variety of industries with the production of medium to large scale software. Career opportunities include: Software Engineer, Systems Administrator, Systems Designer and Tester, Software Analysis, and Programmer.<br><br>Graduates will:</p>
		
		

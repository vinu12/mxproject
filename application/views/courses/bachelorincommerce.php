		<h3>Course Outline</h3>
		
		<p>Your pathway to a rewarding career helping clients makes better financial decisions. This program gives you a strong foundation to be successful in an accountancy role in any industry. You’ll study a major in accounting and gain skills in financial management, business consulting and financial advising. You’ll specialize in areas such as financial accounting, the law of business associations, company accounting, taxation law, and auditing.</p>
		
		
		<p>In addition to completing a major in accounting, choose to complete a major in Financial Planning, a Business major, or a minor and/or electives in another Business area.&nbsp;</p>
		
		
		<p>You’ll also have an opportunity to undertake an internship in your final year, consisting of 96 hours and organized by our staff. Our graduates have found roles in firms such as KPMG and Ernst and Young as a direct result of their internship.</p>
		
		
		<h3>Career opportunities</h3>
		
		<p>Depending on the major chosen: business management, human resource management, marketing management, market research, international business, importing/exporting, industrial relations, tourism and hospitality management, retail/sales, computing, systems analysis.</p>
		
		<p><strong>Program requirements</strong></p>
		
		<p>In order to graduate you must:</p>
		
		<p>• Complete 6 required introductory level (100 coded) courses<br>• Complete 10 required advanced level (200/300 coded) courses<br>• Complete 8 elective courses, 4 must be advanced level (200/300 coded) courses<br>• Complete no more than 10 introductory level (100 coded) courses, including the core course<br>• Complete no more than 6 courses from the School of Communication, School of Social Sciences or Faculty of Science, Health, Education and Engineering<br>• (Optional) Use electives to complete a major (8 courses) or minor (4 courses)<br>• Register any selected majors or minors with the Faculty of Arts and Business.</p>
		
